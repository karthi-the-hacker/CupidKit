    def generate_qr_code(public_url):
        """Generate QR code for the public URL"""
        try:
            qr = qrcode.QRCode(
                version=1,
                error_correction=qrcode.constants.ERROR_CORRECT_L,
                box_size=10,
                border=2,
            )
            qr.add_data(public_url)
            qr.make(fit=True)
            
            img = qr.make_image(fill_color={self.theme["primary"]}, 
                               back_color={self.theme["background_light"]})
            img.save(str(FILES["qrcode"]))
            return FILES["qrcode"]
        except Exception as e:
            print(f"Error generating QR code: {e}")
            return None
    
